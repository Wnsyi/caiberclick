/**
 * 生成完整处方单
 * @param mbtiType MBTI类型
 * @param animal 动物比喻
 * @param animalDesc 动物比喻描述
 * @returns PrescriptionData
 */
export function generatePrescription(
  mbtiType: string,
  animal: string,
  animalDesc: string
): PrescriptionData {
  const template = PRESCRIPTION_DB[mbtiType];

  // 如果某个MBTI类型没有预设，使用随机回退
  if (!template) {
    const fallback = PRESCRIPTION_DB['INFP']; // 默认用INFP
    return {
      mbtiType,
      animal,
      animalDesc,
      ...fallback,
    };
  }

  return {
    mbtiType,
    animal,
    animalDesc,
    ...template,
  };
}

